function exercise26() {

    let input = prompt("Please enter a number between 1 and 7");

    if (input == 1) {
        alert("Monday");
    } else if (input == 2) {
        alert("Tuesday")
    }
    else if (input == 3) {
        alert("Wednesday")
    }
    else if (input == 4) {
        alert("Thursday")
    }
    else if (input == 5) {
        alert("Friday")
    }
    else if (input == 6) {
        alert("Saturday")
    } else if (input == 7) {
        alert("Sunday")
    }
}